// Récupérez tous les éléments de case à cocher dans un tableau
const checkboxes = document.querySelectorAll('input[type="checkbox"]');

// Ajoutez un gestionnaire d'événements "change" à chaque case à cocher
checkboxes.forEach(function(checkbox) {
  checkbox.addEventListener('change', function() {
    let total = 0;
    // Parcourez toutes les cases à cocher et ajoutez leur valeur au total si elles sont cochées
    checkboxes.forEach(function(checkbox) {
      if (checkbox.checked) {
        total += parseFloat(checkbox.parentElement.querySelector('span').textContent.replace('$', ''));
      }
    });
    // Mettez à jour le texte du total
    document.querySelector('.total span').textContent = '$' + total.toFixed(2);
  });
});

</script>
